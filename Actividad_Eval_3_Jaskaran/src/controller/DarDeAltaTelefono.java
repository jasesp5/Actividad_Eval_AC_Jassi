    /**
     * Funcion que crear el telefono y comprueba si los datos introducidos son
     * correctos
     *
     * @return Telefono telefono
     */
    public Telefono crearTelefono() {
        try {
            String nombre = jTextFieldNombre.getText();
            LocalDate fechaAlta = LocalDate.now();
            Telefono telefono = new Telefono(nombre);
            telefono.setFechaDeAlta(fechaAlta);
            return telefono;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, Castellano.ERROR_AL_INTRODUCIR_DATOS);

        }
        return null;

    }


