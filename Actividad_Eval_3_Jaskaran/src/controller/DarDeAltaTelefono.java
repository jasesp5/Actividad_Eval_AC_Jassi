    private void jButtonCrearTelefonoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearTelefonoActionPerformed
        // TODO add your handling code here:
       
        try {
            if (crearTelefono() != null) {
                if (!this.jTextFieldNombre.getText().isEmpty()) {
                    Telefono telefono = crearTelefono();
                    this.telefonoService.insertarTelefono(telefono);
                    JOptionPane.showMessageDialog(null, Castellano.SE_HA_INSERTADO_CORRECTAMENTE_TELEFONO);
                    volverAbrirLaVentana();
                } else {
                    JOptionPane.showMessageDialog(null, Castellano.CAMPO_NOMBRE_VACIO);
                }

            }
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, Castellano.ERROR_AL_CREAR_TELEFONO);

        }
    }//GEN-LAST:event_jButtonCrearTelefonoActionPerformed

